# casting to integer
x = int(1)   # x will be 1
print("type(x) = {0} : x = {1}".format(type(x),x))
x = int(2.8) # y will be 2
print("type(x) = {0} : x = {1}".format(type(x),x))
x = int("3") # z will be 3
print("type(x) = {0} : x = {1}".format(type(x),x))

#casting to float
x = float(1)     # x will be 1.0
print("type(x) = {0} : x = {1}".format(type(x),x))
x = float(2.8)   # y will be 2.8
print("type(x) = {0} : x = {1}".format(type(x),x))
x = float("3")   # z will be 3.0
print("type(x) = {0} : x = {1}".format(type(x),x))
x = float("4.2") # w will be 4.2
print("type(x) = {0} : x = {1}".format(type(x),x))


#casting to string
x = str("s1") # x will be 's1'
print("type(x) = {0} : x = {1}".format(type(x),x))
x = str(2)    # y will be '2'
print("type(x) = {0} : x = {1}".format(type(x),x))
x = str(3.0)  # z will be '3.0'
print("type(x) = {0} : x = {1}".format(type(x),x))
